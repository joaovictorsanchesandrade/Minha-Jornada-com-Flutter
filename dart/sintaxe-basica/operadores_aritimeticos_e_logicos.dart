void main(){
  // Aritimeticos
  int soma = 2 + 2; // Soma
  int subtracao = 2 - 1; // Subtracao
  int multiplicacao = 2 * 2; // Multiplicacao
  double divisao = 2 / 2; // Divisao
  int divisaoInteira = 2 ~/ 2; // Divisao inteira
  int resto = 2 % 2; // Resto da divisao

  print('Resultado soma: $soma');
  print('Resultado subtracao: $subtracao');
  print('Resultado multiplicacao: $multiplicacao');
  print('Resultado divisao: $divisao');
  print('Resultado divisao inteira: $divisaoInteira');
  print('Resultado resto: $resto');


  // Relacionais
  // Igual 
  5 == 5; // > True

  // Diferente
  5 != 5; // > False

  // Maior que
  5 > 5; // > False
  
  // Menor que
  5 < 5; // > False

  // Maior ou igual a
  5 >= 5; // True

  // Menor ou igual a
  5 <= 5;

  // Logicos
  // and
  true && false; // False
  
  // Ou
  true || false; // False

  // Negacao
  !true; // False
}